export type MasteryStatus = 'mastered' | 'learning' | 'locked';

export interface CurriculumNode {
  id: string;
  label: string;
  type: 'domain' | 'topic' | 'subtopic' | 'bridge'; // 'bridge' = auto-generated
  status: MasteryStatus;
  color?: string;
  ringColor?: string;
  prerequisites: string[]; // Node IDs required to unlock this one
  description?: string;
  isAutoGenerated?: boolean; // True if generated by recursive curriculum evolution
  generatedReason?: string;  // Why it was created
}

export interface CurriculumInsert {
  node: CurriculumNode;
  insertBetween: { parent: string; child: string };
  reason: string;
  timestamp: number;
}

